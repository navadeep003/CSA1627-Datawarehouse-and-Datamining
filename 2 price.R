prices <- c(1, 1, 5, 5, 5, 5, 5, 8, 8, 10, 10, 10, 10, 12, 14, 14, 14, 15, 15, 15, 15, 15, 15, 18, 18, 18,18, 18, 18, 18, 18, 20, 20, 20, 20, 20, 20, 20, 21, 21, 21, 21, 25, 25, 25, 25, 25, 28, 28, 30)
summary(prices)   
table(prices)  # Frequency count
mode_price <- as.numeric(names(sort(table(prices), decreasing = TRUE)[1]))
cat("Mode:", mode_price, "\n")
median_price <- median(prices)
cat("Median:", median_price, "\n")
mean_price <- mean(prices)
cat("Mean:", mean_price, "\n")
range_price <- range(prices)
cat("Range:", range_price[2] - range_price[1], "\n")
hist(prices, breaks = 10, main = "Price Distribution", xlab = "Price", ylab = "Frequency")
boxplot(prices, main = "Price Distribution", ylab = "Price")
barplot(table(prices), main = "Price Frequencies", xlab = "Price", ylab = "Frequency")
